	SoundEffect(String name,int id) {
		this.name = name;
		this.id = id;
	}
	
	public boolean isAvariable(BigClientVersion version){
		return avariableVersions.contains(version);
	}
	
	public String getName(){
		return name;
	}
	public int getId() {
		return id;
	}
